type WidenMessageValues<T> = {
  readonly [K in keyof T]: T[K] extends string ? string : WidenMessageValues<T[K]>;
};

export type RiderMessages = WidenMessageValues<(typeof riderMessages)[typeof DEFAULT_RIDER_LOCALE]>;

export function isRiderLocale(value: unknown): value is RiderLocale {
  return typeof value === 'string' && RIDER_LOCALES.includes(value as RiderLocale);
}

export function normalizeRiderLocale(value: unknown): RiderLocale {
  return isRiderLocale(value) ? value : DEFAULT_RIDER_LOCALE;
}

export function getRiderMessages(locale: RiderLocale): RiderMessages {
  return (riderMessages[locale] ?? riderMessages[DEFAULT_RIDER_LOCALE]) as RiderMessages;
}
